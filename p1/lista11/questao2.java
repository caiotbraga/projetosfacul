package p1.lista11;

public class questao2 {
    public static void main(String[] args) {
        int cont, cubo;
        for (cont = 1; cont <= 25; cont = cont + 1) {
            System.out.print(cont + " ");
            cubo = cont * cont * cont;
            System.out.println(cubo);
        }
    }
}
